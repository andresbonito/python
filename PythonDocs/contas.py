a = int(input("Digite o 1° número inteiro: "))
b = int(input("Digite o 2° número inteiro: "))
c = float(input("Digite um número real: "))
produto = (2*a)*(b/2)
soma = (3*a) + c
elevado = c**3

print("O produto será igual a %.2f" % produto)
print("A soma será igual a %.2f" % soma)
print("O numero real elevado ao cubo será igual a %.2f" % elevado)
